console.log(`Addition is: ${result}`);

console.log("--------- Substraction operator----------");
var n1=50;
var n2=20;
var result=n1-n2;
console.log(`Substraction is: ${result}`);

console.log("--------- Multiplication operator----------");
var n1=5;
var n2=6;
var result=n1*n2;
console.log(`Multiplication is: ${result}`);

console.log("--------- Exponential ** operator----------");
var n1=3;
var n2=3;
var result=n1**n2;
console.log(`Exponential is: ${result}`);

console.log("--------- Division operator----------");
var n1=6;
var n2=3;
var result=n1/n2;
console.log(`Division is: ${result}`);

console.log("--------- Modulus operator----------");
var n1=6;
var n2=5;
var result=n1%n2;
console.log(`Modulus is: ${result}`);

console.log("-----------Increment Operator---------");
var n1=10;
var result=++n1;
var n2=10;
var resultValue=n2++;
