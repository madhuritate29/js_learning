
console.log(`---- WAP to print number from 5 to 15 by incrementing 1 ----`);

for (let i = 5; i <=15 ; i++) {
    console.log(i); 
}

console.log(`---- WAP to print number from 50 to 40 by decrementing 1 ----`);
for (let i = 50; i >=40 ; i--) {
    console.log(i); 
}

